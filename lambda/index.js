
const Alexa = require('ask-sdk-core');
const axios = require('axios');
const logic = require('./logic');
const interceptors = require('./interceptors');

const LaunchRequestHandler = {
    canHandle(handlerInput) {
        return Alexa.getRequestType(handlerInput.requestEnvelope) === 'LaunchRequest';
    },
    handle(handlerInput) {
        const speakOutput = handlerInput.t('WELCOME_MSG');

        return handlerInput.responseBuilder
            .speak(speakOutput)
            .reprompt(speakOutput)
            .getResponse();
    }
};

const GetLocationIntentHandler = {
    canHandle(handlerInput) {
        return Alexa.getRequestType(handlerInput.requestEnvelope) === 'IntentRequest'
            && Alexa.getIntentName(handlerInput.requestEnvelope) === 'GetLocation';
    },
    async handle(handlerInput) {
        var speakOutput = handlerInput.t('CALCULATING_SOUND');
        var coordinate = await logic.fetchSatelliteData(handlerInput, 'location');
        var latitude = coordinate[0];
        var longitude = coordinate[1];
        var location = await logic.reverseGeocode(latitude, longitude);
        speakOutput += "At this moment, the International Space Station is at the geographic coordinate "
            + "(" + latitude + ", " + longitude + ") which is right above the " + location +" on Earth.";
        return handlerInput.responseBuilder
            .speak(speakOutput)
            .reprompt(speakOutput)
            .withShouldEndSession(false)
            .getResponse();
    }
}

